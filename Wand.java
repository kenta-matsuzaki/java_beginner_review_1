public class Wand {
  private String name;
  private double power;

  // nameのゲッターとセッター
  public String getName(){
    return this.name;
  }
  public void setName(String name){
    this.name = name;
  }

  // powerのゲッターとセッター
  public double getPower(){
    return this.power;
  }
  public void setPower(double power){
    this.power = power;
  }
}